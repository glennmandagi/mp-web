Manado Post adalah jaringan media terbesar di Sulawesi Utara, yang   memimpin pasar koran-koran lokal di Kota Manado dan daerah sekitarnya   (Provinsi Gorontalo, Sulawesi Tengah dan Maluku Utara). Sebagai media   terkemuka, sejak tahun 1986 Manado Post terus menyajikan informasi ke   masyarakat secara terkini dan terpercaya.<br />
<br />
Dengan kekuatan jaringan tersebut, Manado Post menghadirkan <a href="http://manadopostonline.com" target="_blank">manadopostonline.com</a>,   layanan portal berita dengan suguhan direktori lengkap berita   Nusantara, Nasional dan Internasional, realtime news peristiwa-peristiwa   terkini, serta rubrik-rubrik lain yang tak kalah menarik.<br />
